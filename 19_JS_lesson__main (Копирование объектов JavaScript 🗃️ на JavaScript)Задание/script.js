/**
 * Функция для создания полной (глубокой) копии объекта
 */
function deepCopy(obj) {
    // 1. Проверка на примитивы: если это не объект или это null,
    // то копировать нечего — возвращаем само значение.
    // Это "базовый случай" рекурсии, чтобы она не стала бесконечной.
    if (obj === null || typeof obj !== 'object') {
        return obj;
    }

    // 2. Определяем структуру: если оригинал — массив, создаем новый пустой массив [].
    // Если объект — создаем новый пустой объект {}.
    // Это гарантирует сохранение типа данных (Array или Object).
    let copy = Array.isArray(obj) ? [] : {};

    // 3. Перебираем все ключи (свойства) в исходном объекте.
    for (let key in obj) {
        // 4. Проверяем, принадлежит ли свойство именно этому объекту,
        // а не его прототипу (чтобы не копировать лишние методы "от родителей").
        if (Object.prototype.hasOwnProperty.call(obj, key)) {

            // 5. САМОЕ ГЛАВНОЕ: Рекурсивный вызов.
            // Мы не просто копируем значение, а снова вызываем deepCopy для этого значения.
            // Если там внутри другой объект — функция уйдет вглубь.
            // Если там примитив — сработает условие из шага №1 и значение просто вернется.
            copy[key] = deepCopy(obj[key]);
        }
    }

    // 6. Возвращаем полностью собранный новый объект/массив.
    return copy;
}