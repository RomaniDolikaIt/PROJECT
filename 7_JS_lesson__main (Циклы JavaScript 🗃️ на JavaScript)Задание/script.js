console.log("Простые числа от 0 до 100:");

// Первый цикл перебирает числа, которые мы проверяем (начинаем с 2, т.к. 0 и 1 не простые)
for (let i = 2; i <= 100; i++) {
    let isPrime = true; // Предполагаем, что число простое

    // Второй цикл проверяет, делится ли наше число 'i' на что-то другое
    for (let j = 2; j < i; j++) {
        if (i % j === 0) {
            isPrime = false; // Нашли делитель! Значит число не простое
            break; // Дальше проверять нет смысла, выходим из внутреннего цикла
        }
    }

    // Если после проверки флаг остался true — выводим число
    if (isPrime) {
        console.log(i);
    }
}