//todo 2. Напиши программу, которая использует цикл while, чтобы посчитать сумму чисел от 1 до 100;

let sum = 0;    // Наша "копилка", в начале она пустая
let count = 1;  // Первая "монетка", которую мы берем

// Пока (while) счетчик не превысил 100
while (count <= 100) {
    sum = sum + count; // Кладём текущее число в общую сумму
    count = count + 1; // Увеличиваем число на 1 для следующего шага
}

console.log("Сумма чисел от 1 до 100 равна:", sum);

