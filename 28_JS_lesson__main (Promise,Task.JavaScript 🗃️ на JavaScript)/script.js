//todo 1. Напиши функцию `getUserData`, которая возвращает промис с данными пользователя
// через 2 секунды. Затем создай цепочку промисов, которая обрабатывает эти данные и выводит
// результат в консоль;

// 1. Функция, которая возвращает промис с данными
function getUserData() {
    return new Promise((resolve) => {
        console.log("⏳ Запрос к серверу... Получение данных пользователя...");

        // Имитируем задержку сети в 2 секунды (2000 мс)
        setTimeout(() => {
            const user = {
                id: 42,
                name: "Алексей",
                role: "user"
            };
            resolve(user); // "Выстреливаем" данные наружу
        }, 2000);
    });
}

// 2. Создаем цепочку промисов (Promise Chaining)
getUserData()
    .then((user) => {
        console.log("📦 Шаг 1 (Данные получены):", user);
        // Модифицируем данные: переводим имя в верхний регистр
        user.name = user.name.toUpperCase();
        // Обязательно возвращаем измененный объект, чтобы он полетел в следующий .then
        return user;
    })
    .then((processedUser) => {
        console.log(`🛠️ Шаг 2 (Имя обработано): Теперь имя пишется как ${processedUser.name}`);
        // Добавляем новое свойство объекту
        processedUser.isVerified = true;
        return processedUser;
    })
    .then((finalUser) => {
        // Финальный аккорд — выводим итоговый объект
        console.log("🚀 Шаг 3 (Финальный результат):", finalUser);
    })
    .catch((error) => {
        // Если бы на каком-то этапе произошла ошибка, мы бы упали сюда
        console.error("Произошла ошибка при обработке:", error);
    });

//todo 2. Напиши две функции, каждая из которых возвращает промис с данными через 3 и 5 секунд
// соответственно. Используй такой метод промисов, чтобы дождаться выполнения обоих промисов и вывести результаты в консоль;

// 1. Первая функция — возвращает данные через 3 секунды
function getFastData() {
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve("⚡ Данные за 3 секунды получены!");
        }, 3000);
    });
}

// 2. Вторая функция — возвращает данные через 5 секунд
function getSlowData() {
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve("🐢 Данные за 5 секунд получены!");
        }, 5000);
    });
}

console.log("⏳ Запускаем обе операции параллельно...");
console.time("Время ожидания"); // Включаем внутренний секундомер в консоли

// 3. Используем Promise.all для одновременного ожидания
Promise.all([getFastData(), getSlowData()])
    .then((results) => {
        console.log("🎉 Оба промиса успешно завершились!");

        // В аргумент results прилетает МАССИВ с ответами в том же порядке,
        // в каком мы передавали функции: [ответ_1, ответ_2]
        console.log("Результаты из массива:", results);

        console.log("Первый ответ:", results[0]);
        console.log("Второй ответ:", results[1]);

        console.timeEnd("Время ожидания"); // Выключаем секундомер и смотрим время
    })
    .catch((error) => {
        // Если бы хоть один промис упал с ошибкой (reject), мы бы мгновенно оказались здесь
        console.error("Произошла ошибка в одном из промисов:", error);
    });

//todo 3. Напиши две функции, каждая из которых возвращает промис через случайное время
// (от 1 до 5 секунд). Используй такой метод промисов, чтобы вывести результат первого
// выполненного промиса в консоль.


// Функция-помощник для генерации случайного числа секунд от 1 до 5
function getRandomTime() {
    return Math.floor(Math.random() * 5000) + 1000; // от 1000 до 5000 миллисекунд
}

// 1. Первый участник гонки
function runnerOne() {
    const time = getRandomTime();
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve(`🏃 Участник №1 прибежал за ${(time / 1000).toFixed(1)} сек!`);
        }, time);
    });
}

// 2. Второй участник гонки
function runnerTwo() {
    const time = getRandomTime();
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve(`⚡ Участник №2 прибежал за ${(time / 1000).toFixed(1)} сек!`);
        }, time);
    });
}

console.log("🏁 Гонка началась! Ждем первого победителя...");

// 3. Запускаем Promise.race
Promise.race([runnerOne(), runnerTwo()])
    .then((winnerMessage) => {
        console.log("🏆 Результат гонки:");
        // Сюда прилетит строка ТОЛЬКО от того промиса, у которого таймер сработал быстрее
        console.log(winnerMessage);
    })
    .catch((error) => {
        // Если бы самый быстрый промис сломался (reject), мы бы упали сюда
        console.error("Самый быстрый промис завершился ошибкой:", error);
    });