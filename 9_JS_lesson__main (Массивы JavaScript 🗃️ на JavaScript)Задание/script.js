//todo 1. Есть массив чисел. Преобразуй массив так,
// чтобы там были квадраты чисел. Пример: [2, 3] -> [4, 9];

const numbers = [2, 3, 4, 5];

// На каждой итерации берем число (n) и умножаем его на самого себя
const squares = numbers.map(n => n * n);

console.log(squares); // [4, 9, 16, 25]

//todo 2. Есть массив c повторяющимися элементами. Отфильтруй массив так,
// чтобы там остались только уникальные элементы.
// Пример: [1, 2, 2, 3, 4, 5, 5, 5, 6] -> [1, 2, 3, 4, 5, 6];

const numbers3 = [1, 2, 2, 3, 4, 5, 5, 5, 6];

const unique = numbers3.filter((num, index, array) => {
    // Проверяем: совпадает ли индекс текущего элемента
    // с индексом его самого первого появления в массиве?
    return array.indexOf(num) === index;
});

console.log(unique); // [1, 2, 3, 4, 5, 6]


//todo 3. Есть массив чисел, посчитай сумму его элементов.
// Пример: [1, 2, 3] -> 6. Подсказка: используй reduce;

const numbers1 = [1, 2, 3, 4, 5];

const finalSum = numbers1
.reduce((total, number1) => total + number1, 0);
console.log(finalSum);


