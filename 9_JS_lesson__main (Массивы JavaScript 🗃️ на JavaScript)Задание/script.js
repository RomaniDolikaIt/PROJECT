/*
   Объясняю своими словами, почему так:
Представь, что переменная — это рука, которая указывает на коробку (массив).
Случай с let: Твоя рука свободна. Ты можешь переложить вещи внутри коробки,
а можешь вообще убрать руку от этой коробки и указать на совсем другую.
Случай с const: Твоя рука приклеена намертво к конкретной коробке.
Ты не можешь перевести руку на другой массив (переопределить его). НО!
Твои пальцы свободны. Ты можешь залезть внутрь этой коробки,
выкинуть оттуда «Poland» или положить туда «Germany».

Итог:
const защищает не содержимое коробки, а связь переменной с этой коробкой.

Большинство JS-разработчиков используют const для массивов и объектов всегда,
если не планируют полностью заменять их на новые.
Это страхует от случайной перезаписи всей переменной.
 */