//(4). Сравни null и undefined строго (===) и не строго (==), выведи результаты в консоль.
// Объясни своими словами, почему получились такие результаты

console.log("Строгое сравнение (===):");
console.log(null === undefined); // false

console.log("Нестрогое сравнение (==):");
console.log(null == undefined);  // true
/* Объяснение своими словами
Почему так получается?
1. Строгое сравнение (===) → false
Здесь всё максимально логично. Оператор === проверяет два условия: тип данных и значение.

typeof null — это 'object' (известная старая ошибка в JS, но формально это так).

typeof undefined — это 'undefined'.

Так как типы разные, проверка сразу возвращает false. Они не идентичны.

2. Нестрогое сравнение (==) → true
А вот здесь начинается «магия» JavaScript. Обычно оператор == пытается привести типы к общему знаменателю (например, превращает строку '5' в число 5).

Однако для null и undefined в спецификации языка прописано особое правило:

Эти два значения «равны» друг другу при нестрогом сравнении, но не равны больше ничему в языке (кроме самих себя).
 */

