// Внешняя функция-фабрика
function createDiscountCalculator(discount) {
    // Переменная discount (размер скидки) объявляется здесь, во внешней функции.
    // Она сохраняется в "рюкзаке" (лексическом окружении).

    // Возвращаем внутреннюю функцию
    return function(price) {
        // Внутренняя функция имеет доступ к discount через замыкание,
        // хотя внешняя функция уже давно завершит свою работу!
        const finalPrice = price - (price * (discount / 100));

        // Выводим результат в консоль
        console.log(`Цена со скидкой ${discount}% составит: ${finalPrice} руб.`);
    };
}

