function showThis() {
  return this;
}

const whatIsThis = showThis();
printToScreen(
  "res-3",
  `🧭 АНАЛИЗ КОНТЕКСТА (this):\nЗначение "this" сейчас равно: ${whatIsThis}`,
);
printToScreen(
  "res-3",
  `\n💡 Почему так?\nВ обычном режиме JS подставил бы сюда глобальный объект Window.\nНо завуч обнулил его до undefined ради безопасности, чтобы мы случайно не сломали глобальные настройки браузера.`,
);

