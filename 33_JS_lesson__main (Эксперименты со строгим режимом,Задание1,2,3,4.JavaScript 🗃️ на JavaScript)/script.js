// Включаем строгого завуча на весь файл!
"use strict";

// Вспомогательная функция для вывода текста в наши кубики на экране
function printToScreen(elementId, text) {
  document.getElementById(elementId).textContent += text + "\n";
}

// ==========================================
// 🛠️ ЗАДАНИЕ 1: Переменная без объявления
// ==========================================
try {
  function addNumbers(a, b) {
    // Пытаемся создать переменную без let/const
    result = a + b;
    return result;
  }
  addNumbers(5, 10);
} catch (error) {
  printToScreen(
    "res-1",
    `❌ ЗАВУЧ ПОЙМАЛ ОШИБКУ:\n${error.name}: ${error.message}`,
  );

  // Исправляем код прямо здесь, чтобы показать как надо:
  function addNumbersFixed(a, b) {
    const result = a + b; // <-- Исправлено! Добавили const
    return result;
  }
  printToScreen(
    "res-1",
    `\n✅ ИСПРАВЛЕННЫЙ КОД:\nРезультат работы исправительной функции: ${addNumbersFixed(5, 10)}`,
  );
}

// ==========================================
// 🛠️ ЗАДАНИЕ 2: Параметры с одинаковыми именами
// ==========================================

// ВАЖНЫЙ НЮАНС: Ошибку дублирования аргументов (function sum(a, a))
// завуч проверяет ЕЩЕ ДО запуска кода (на этапе парсинга).
// Если я запишу её текстом прямо в код, весь файл просто откажется работать.
// Поэтому мы проверим её хитро — сэмулируем через чтение строки:
try {
  // Пытаемся заставить браузер прочитать функцию-близнеца в строгом режиме
  eval('"use strict"; function badSum(a) { return a + a; }');
} catch (error) {
  printToScreen(
    "res-2",
    `❌ ЗАВУЧ ПОЙМАЛ ОШИБКУ:\n${error.name}: ${error.message}`,
  );

  // Исправляем ошибку: даем параметрам РАЗНЫЕ понятные имена
  function correctSum(num1, num2) {
    // <-- Исправлено! Аргументы уникальны
    return num1 + num2;
  }
  printToScreen(
    "res-2",
    `\n✅ ИСПРАВЛЕННЫЙ КОД:\nРезультат правильной функции (10 + 20): ${correctSum(10, 20)}`,
  );
}

