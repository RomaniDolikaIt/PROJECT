//todo 1. Напиши рекурсивную функцию для вычисления суммы всех элементов в массиве;

function sumArray(arr) {
    // 1. ТОРМОЗ (Базовый случай)
    // Если массив пустой (длина === 0), то складывать нечего, возвращаем 0.
    if (arr.length === 0) {
        return 0;
    }

    // 2. ПЕДАЛЬ ГАЗА (Рекурсивный шаг)
    // Мы берем ПЕРВЫЙ элемент массива: arr[0]
    // И прибавляем к нему результат вызова этой же функции sumArray,
    // но передаем туда остаток массива БЕЗ первого элемента: arr.slice(1)
    return arr[0] + sumArray(arr.slice(1));
}

// === ПРОВЕРЯЕМ В КОНСОЛИ ===
const numbers = [1, 2, 3, 4, 5];
console.log("Сумма массива:", sumArray(numbers)); // Выведет: 15

//todo 2. Реализуй функцию для нахождения максимального элемента в массиве с использованием рекурсии;

function findMax(arr) {
    // 1. ТОРМОЗ (Базовый случай)
    // Если в массиве остался всего один элемент, то он и есть максимальный!
    if (arr.length === 1) {
        return arr[0];
    }

    // 2. ПЕДАЛЬ ГАЗА (Рекурсивный шаг)
    // Находим максимум во ВСЕМ ОСТАЛЬНОМ массиве (без первого элемента)
    const maxOfRemaining = findMax(arr.slice(1));

    // Сравниваем первый элемент с максимумом остальной части
    if (arr[0] > maxOfRemaining) {
        return arr[0]; // Если первый больше, возвращаем его
    } else {
        return maxOfRemaining; // Иначе возвращаем максимум остальной части
    }
}

// === ПРОВЕРЯЕМ В КОНСОЛИ ===
const numbers = [3, 8, 1, 12, 5];
console.log("Максимальный элемент:", findMax(numbers)); // Выведет: 12

//todo 4. А вот теперь нужно реализовать функцию для вычисления чисел Фибоначчи с кэшированием
// через рекурсию! Требования те же, что и в предыдущем уроке.

function createFibonacciCalculator() {
    // Шаг 1: СКЛАД (Замыкание)
    // Создаем наш блокнот для кэширования.
    // Сразу записываем базовые случаи: для 0 это 0, для 1 это 1.
    const cache = {
        0: 0,
        1: 1
    };

    // Шаг 2: РОБОТ-ВЫЧИСЛИТЕЛЬ
    // Возвращаем функцию, которая будет вызывать саму себя рекурсивно
    return function fib(n) {

        // Шаг 3: ТОРМОЗ / ПРОВЕРКА БЛОКНОТА
        // Первым делом проверяем, есть ли уже готовый ответ в блокноте.
        // Если n равен 0 или 1, ответ уже там! Если мы уже считали это n раньше — ответ тоже там!
        if (n in cache) {
            return cache[n]; // Мгновенно возвращаем готовое число
        }

        // Шаг 4: ПЕДАЛЬ ГАЗА (Рекурсивный шаг) + ЗАПИСЬ
        // Если в блокноте ответа нет, мы считаем его рекурсивно:
        // Число Фибоначчи — это сумма двух предыдущих чисел: (n - 1) и (n - 2).
        // Мы вызываем функцию fib для них, а результат О Б Я З А Т Е Л Ь Н О записываем в кэш!
        cache[n] = fib(n - 1) + fib(n - 2);

        // Шаг 5: Возвращаем посчитанный результат
        // Теперь он сохранен в блокноте и в следующий раз посчитается мгновенно
        return cache[n];
    };
}

// === ПРОВЕРЯЕМ В КОНСОЛИ ===
const fibonacci = createFibonacciCalculator();

console.log(fibonacci(0));  // 0 (взял из кэша)
console.log(fibonacci(1));  // 1 (взял из кэша)
console.log(fibonacci(5));  // 5 (посчитал рекурсией и сохранил)
console.log(fibonacci(10)); // 55 (посчитал рекурсией и сохранил)

// Без кэша рекурсия на числе 50 зависла бы намертво и выдала ошибку.
// А с кэшем — выведется мгновенно!
console.log(fibonacci(50)); // 12586269025