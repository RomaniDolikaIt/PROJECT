//todo 1. Напиши асинхронную функцию `delay`, которая принимает один аргумент -
// количество миллисекунд, и возвращает промис, который разрешается (резолвится)
// через заданное количество времени. Используй `async/await` для ожидания этого промиса
// и выведите сообщение "Задержка завершена" после завершения ожидания;

// 1. Вспомогательная функция, которая просто возвращает промис-таймер
function delay(ms) {
    return new Promise((resolve) => {
        // Промис успешно разрешится (вызовется resolve) ровно через ms миллисекунд
        setTimeout(resolve, ms);
    });
}

// 2. Асинхронная функция, которая использует await для ожидания
async function runTimer() {
    console.log("🚀 Начинаем ожидание задержки...");

    // Ключевое слово await буквально "ставит код на паузу" в этой строчке.
    // Код замрет на 3000 миллисекунд (3 секунды), пока промис из функции delay не выполнится.
    await delay(3000);

    // Эта строчка НЕ выполнится, пока не пройдут 3 секунды выше
    console.log("✅ Задержка завершена");
}

// Запускаем нашу асинхронную функцию
runTimer();

//todo 2. Напиши асинхронную функцию `fetchUserData`, которая делает запрос к фейковому API (любому)
// и возвращает данные пользователя. Используй функцию fetch

// Асинхронная функция для загрузки данных
async function fetchUserData() {
    try {
        console.log("📡 Отправляем запрос на сервер...");

        // Шаг 1: Делаем запрос. await замораживает код, пока сервер не ответит.
        const response = await fetch('https://jsonplaceholder.typicode.com/users/1');

        // Проверяем, всё ли прошло успешно (статус ответа 200-299)
        if (!response.ok) {
            throw new Error(`Ошибка сервера! Статус: ${response.status}`);
        }

        // Шаг 2: Сервер прислал сырой текст. Переводим его в удобный JS-объект.
        // метод .json() ТОЖЕ асинхронный, поэтому перед ним нужен await!
        const userData = await response.json();

        // Возвращаем чистый объект наружу
        return userData;

    } catch (error) {
        // Если выключится интернет или сервер упадет, мы поймаем ошибку здесь
        console.error("❌ Не удалось загрузить данные:", error.message);
    }
}

// === ЗАПУСК ===
async function run() {
    const user = await fetchUserData();

    if (user) {
        console.log("🎉 Данные успешно получены и обработаны!");
        console.log(`👤 Имя пользователя: ${user.name}`);
        console.log(`📧 Email: ${user.email}`);
        console.log(`🏢 Компания: ${user.company.name}`);
        console.log("📦 Полный объект пользователя из сети:", user);
    }
}

run();