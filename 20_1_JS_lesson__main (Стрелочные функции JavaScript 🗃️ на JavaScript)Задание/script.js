//todo 1. Напиши функцию, которая принимает массив чисел,
// совершает над ними любую математическую операцию и возвращает новый массив,
// содержащий результаты этих операций,
// используя стрелочные функции;

// Функция принимает массив чисел
const squareNumbers = (arr) => arr.map(num => num * num);

// Проверяем работу функции:
const originalArray = [1, 2, 3, 4, 5];
const resultArray = squareNumbers(originalArray);

console.log(originalArray); // [1, 2, 3, 4, 5] (исходный массив остался цел)
console.log(resultArray);   // [1, 4, 9, 16, 25] (новый массив с результатами)

//todo 2. Создай объект с методом, который использует стрелочную функцию внутри метода
// `setTimeout` для вывода значения свойства объекта через 1 секунду;

const user = {
    name: 'Алексей',
    // Метод объекта (обычная функция)
    sayHelloAfterDelay: function() {
        // Стрелочная функция внутри setTimeout
        setTimeout(() => {
            // Стрелка не имеет своего this и забирает контекст из метода sayHelloAfterDelay.
            // А в методе sayHelloAfterDelay контекст — это сам объект user (так как вызов идет через точку).
            console.log(`Привет, меня зовут ${this.name}!`);
        }, 9000);
    }
};

// Вызываем метод объекта
user.sayHelloAfterDelay();
// Через 9 секунд в консоли появится: "Привет, меня зовут Алексей!"

//todo 3. Реализуй функцию высшего порядка*, которая принимает функцию и массив,
// и применяет эту функцию ко всем элементам массива, используя стрелочные функции.

// Функция высшего порядка (принимает колбэк-функцию 'callback' и массив 'arr')
const processArray = (arr, callback) => {
    const result = []; // Создаем новый пустой массив для результатов

    for (const item of arr) {
        // Применяем переданную функцию к каждому элементу и сохраняем результат
        result.push(callback(item));
    }

    return result; // Возвращаем новый массив
};

// --- Проверяем, как это работает ---

const numbers = [2, 4, 6, 8];

// 1. Передаем стрелочную функцию, которая умножает на 10
const multiplied = processArray(numbers, x => x * 10);
console.log(multiplied); // [20, 40, 60, 80]

// 2. Передаем другую стрелочную функцию, которая делает из чисел строки
const strings = processArray(numbers, x => `Число ${x}`);
console.log(strings); // ['Число 2', 'Число 4', 'Число 6', 'Число 8']