//todo 1. Напиши функцию `safeDivide`, которая принимает два числа и возвращает результат их деления.
// Если второй аргумент равен нулю, функция должна бросать ошибку с
// сообщением "Деление на ноль невозможно". Используй `try...catch`
// для обработки ошибок и выведи сообщение об ошибке в консоль;

function safeDivide(a, b) {
    try {
        // Шаг 1: Проверяем, не пытаются ли нас заставить делить на ноль
        if (b === 0) {
            // Если b это ноль, сами вручную жмем на стоп-кран!
            throw new Error("Деление на ноль невозможно");
        }

        // Шаг 2: Если всё отлично, возвращаем результат деления
        return a / b;

    } catch (error) {
        // Шаг 3: Сюда мы прилетим пулей, если сработал throw сверху
        console.log(`❌ Произошла ошибка: ${error.message}`);

        // Возвращаем null или специальное значение, чтобы программа знала, что расчет не удался
        return null;
    }
}

// === ПРОВЕРЯЕМ В КОНСОЛИ ===

// Ситуация 1: Хороший сценарий
const goodResult = safeDivide(10, 2);
console.log("Результат деления:", goodResult); // Выведет: Результат деления: 5

console.log("---------------------------------------");

// Ситуация 2: Попытка деления на ноль
const badResult = safeDivide(10, 0);
console.log("Результат деления:", badResult); // Выведет: null (потому что сработал catch)

//todo 2. Напиши функцию `transfromJSON`, которая принимает строку в формате JSON и возвращает объект.
// Используй `try...catch` для обработки возможных ошибок при парсинге JSON и выведи сообщение об ошибке
// в консоль;

function transformJSON(jsonString) {
    try {
        // Пытаемся превратить строку в реальный объект
        const parsedObject = JSON.parse(jsonString);

        // Если всё прошло успешно, возвращаем этот объект наружу
        return parsedObject;

    } catch (error) {
        // Сюда мы попадём, только если JSON.parse() «взорвётся» из-за кривого синтаксиса
        console.log(`❌ Ошибка при чтении JSON: ${error.message}`);

        // Возвращаем пустой объект или null, чтобы остальной код не сломался
        return null;
    }
}

// === ПРОВЕРЯЕМ В КОНСОЛИ ===

// 1. Правильный JSON (всё сработает штатно)
const goodJson = '{"name": "Марк", "role": "Админ"}';
const userObj = transformJSON(goodJson);
console.log("Успешный объект:", userObj); // Выведет сам объект


console.log("---------------------------------------");


// 2. Сломанный JSON (забыли закрыть фигурные скобки и кавычки)
const badJson = '{"name": "Марк", "role": "Админ';
const invalidObj = transformJSON(badJson);
console.log("Результат при ошибке:", invalidObj); // Выведет: null

//todo 3. Напиши функцию `checkAccess`, которая принимает возраст пользователя. Если возраст меньше 18,
// функция должна бросать ошибку с сообщением "Доступ запрещен". Используйте `try...catch`
// для обработки ошибок и выведи сообщение об ошибке в консоль.

function checkAccess(age) {
    try {
        console.log(`🎫 Проверяем возраст: ${age}...`);

        // Если пользователь несовершеннолетний, активируем стоп-кран!
        if (age < 18) {
            throw new Error("Доступ запрещен");
        }

        // Если условие выше не выполнилось, код спокойно идет дальше
        console.log("✅ Добро пожаловать! Доступ успешно предоставлен.");
        return true;

    } catch (error) {
        // Ловим нашу ошибку "Доступ запрещен"
        console.log(`❌ Стоп! Произошла ошибка: ${error.message}`);
        return false;
    }
}

// === ПРОВЕРЯЕМ В КОНСОЛИ ===

// 1. Попытка входа для взрослого (25 лет)
checkAccess(25);
// Выведет: Проверяем возраст: 25... -> Добро пожаловать! Доступ успешно предоставлен.

console.log("---------------------------------------");

// 2. Попытка входа для подростка (15 лет)
checkAccess(15);
// Выведет: Проверяем возраст: 15... -> Стоп! Произошла ошибка: Доступ запрещен