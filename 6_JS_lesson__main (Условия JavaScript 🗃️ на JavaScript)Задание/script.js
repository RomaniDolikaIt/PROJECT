//2. Напиши программу, которая запрашивает у пользователя его рост и вес,
// на основании этих данных вычисляет ИМТ, и выводит сообщение о том, в каком
// диапазоне находится его ИМТ;

// 1. Запрашиваем данные у пользователя (в браузере это делается через prompt)
let weight = parseFloat(prompt("Введите ваш вес в кг (например, 75):"));
let height = parseFloat(prompt("Введите ваш рост в метрах (например, 1.75):"));

// 2. Вычисляем ИМТ
let bmi = weight / (height * height);

// Округляем для красоты до одного знака после запятой
let roundedBmi = bmi.toFixed(1);

// 3. Проверяем диапазоны по стандартам ВОЗ
if (bmi < 18.5) {
    console.log("Ваш ИМТ: " + roundedBmi + ". Дефицит массы тела.");
} else if (bmi >= 18.5 && bmi < 25) {
    console.log("Ваш ИМТ: " + roundedBmi + ". Нормальный вес.");
} else if (bmi >= 25 && bmi < 30) {
    console.log("Ваш ИМТ: " + roundedBmi + ". Избыточный вес.");
} else {
    console.log("Ваш ИМТ: " + roundedBmi + ". Ожирение.");
}

